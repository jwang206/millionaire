class Purse
  def initialize
    @values = {
      0 => "$100",
      1 => "$200",
      2 => "$300",
      3 => "$500",
      4 => "$1,000",
      5 => "$2,000",
      6 => "$4,000",
      8 => "$8,000",
      9 => "$16,000",
      10 => "$32,000",
      11 => "$64,000",
      12 => "$125,000",
      13 => "$250,000",
      14 => "$500,000",
      15 => "$1,000,000"
    }
    @round = -1
  end

  def get_value
    @values[@round]
  end

  def increment
    @round += 1
  end
end